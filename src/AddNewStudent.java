    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        Connection conn=null;
        PreparedStatement pst=null;
        try{
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/mysql","root","newrootpassword");
            
            if( passwordtext.getText().equals(confirmtext.getText())){
                pst = conn.prepareStatement("insert into onlineexam.studentlogin (username,password,score)VALUES(?,?,?)");
                pst.setString(1, usernametext.getText());
                pst.setString(2, passwordtext.getText());
                pst.setInt(3,-1);
                pst.execute();
                JOptionPane.showMessageDialog(this,"Saved");
                usernametext.setText(null);
                passwordtext.setText(null);
                confirmtext.setText(null);
            }
            else{
                JOptionPane.showMessageDialog(this,"Enter Password Again");
                passwordtext.setText(null);
                confirmtext.setText(null);
            }
        }
        catch(SQLException | HeadlessException e){
            System.out.println("Error"+e);
        }    
        finally {
            try { pst.close(); } catch (Exception e) { /* ignored */ }
            try { conn.close(); } catch (Exception e) { /* ignored */ }
        }
    }//GEN-LAST:event_addActionPerformed

